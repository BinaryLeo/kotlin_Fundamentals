fun main(){
    // Kotlin operators
    val a = 23.0
    val b = 12.0
    println(a + b)// 35.0
    println(a - b) //11.0
    println(a / b) // 1.9166666666666667

    val c = 10
    val d = 2
    val e = 3
    println(c % d) // 0  is the remainder
    println(c%e)// 1 is the remainder
    println(c * d) // 20
}